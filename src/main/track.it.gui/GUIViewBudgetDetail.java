    private void submitEditTitleBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitEditTitleBtnActionPerformed
        try{
            if(newTitleInput.getText().trim().isEmpty()){
                msgLabelEditTitle.setText("New title cannot be left blank.");
                msgLabelEditTitle.setForeground(Color.red);
            }else{
                budgetList.getBudgetItem(budgetIndex).setTitle(newTitleInput.getText());
                bgTitleContent.setText(budgetList.getBudgetItem(budgetIndex).getTitle());
                editTitleDialog.dispose();
                newTitleInput.setText("");
                msgLabelEditExp.setText("");
            }
        }catch(TitleHasCommaException titleCommaException){
            msgLabelEditTitle.setText(titleCommaException.titleHasCommaMsg());
            msgLabelEditTitle.setForeground(Color.red);
        }
    }//GEN-LAST:event_submitEditTitleBtnActionPerformed

