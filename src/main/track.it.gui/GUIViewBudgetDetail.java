    private void confirmRemoveExpBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmRemoveExpBtnActionPerformed
        DefaultTableModel editExpTableModel = (DefaultTableModel) editExpTable.getModel();
        DefaultTableModel expTableModel = (DefaultTableModel) expTable.getModel();
        int selectedRow = editExpTable.getSelectedRow();
        Expense selectedExpense = budgetList.getBudgetItem(budgetIndex).getExpenseItem(selectedRow);
        budgetList.getBudgetItem(budgetIndex).removeExpense(selectedExpense);
        editExpTableModel.removeRow(selectedRow);
        editExpPurposeInput.setText("");
        editExpAmtInput.setText("");

        //update total expense and total balance
        expTableModel.removeRow(selectedRow);
        totalExpContent.setText(Integer.toString(budgetList.getBudgetItem(budgetIndex).getTotalExpense()));
        balanceContent.setText(Integer.toString(budgetList.getBudgetItem(budgetIndex).getBalance()));

        removeExpDialog.dispose();
        msgLabelEditExp.setText("The selected expense has been removed.");
    }//GEN-LAST:event_confirmRemoveExpBtnActionPerformed

