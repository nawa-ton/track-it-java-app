    private void editExpUpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editExpUpdateBtnActionPerformed
        DefaultTableModel editExpTableModel = (DefaultTableModel) editExpTable.getModel();
        DefaultTableModel expTableModel = (DefaultTableModel) expTable.getModel();
        int selectedRow = editExpTable.getSelectedRow();
        try{
            if(selectedRow == -1){
                msgLabelEditExp.setText("No selected item. Please select the item you want by clicking its row.");
                msgLabelEditExp.setForeground(Color.red);
            }else{
                if(editExpPurposeInput.getText().trim().equals("")){
                    msgLabelEditExp.setText("Expense purpose cannot be blank.");
                    msgLabelEditExp.setForeground(Color.red);
                }else{
                    msgLabelEditExp.setText("");
                    //update expense info
                    Expense selectedExpense = budgetList.getBudgetItem(budgetIndex).getExpenseItem(selectedRow);
                    selectedExpense.setExpensePurpose(editExpPurposeInput.getText());
                    selectedExpense.setExpenseAmount(Integer.parseInt(editExpAmtInput.getText()));
                    editExpTableModel.setValueAt(editExpPurposeInput.getText(), selectedRow, 0);
                    editExpTableModel.setValueAt(editExpAmtInput.getText(), selectedRow, 1);

                    //update total expense and total balance
                    expTableModel.setValueAt(editExpPurposeInput.getText(), selectedRow, 0);
                    expTableModel.setValueAt(editExpAmtInput.getText(), selectedRow, 1);
                    totalExpContent.setText(Integer.toString(budgetList.getBudgetItem(budgetIndex).getTotalExpense()));
                    balanceContent.setText(Integer.toString(budgetList.getBudgetItem(budgetIndex).getBalance()));
                }
            }
        }catch(NumberFormatException nfe){
            msgLabelEditExp.setText("Budget amount is not an integer. Please re-enter.");
            msgLabelEditExp.setForeground(Color.red);
        }catch (ExpensePurposeHasCommaException expenseCommaException){
            msgLabelEditExp.setText(expenseCommaException.purposeCommaMsg());
            msgLabelEditExp.setForeground(Color.red);
        }
    }//GEN-LAST:event_editExpUpdateBtnActionPerformed

    private void editExpInsertBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editExpInsertBtnActionPerformed
        DefaultTableModel editExpTableModel = (DefaultTableModel) editExpTable.getModel();
        DefaultTableModel expTableModel = (DefaultTableModel) expTable.getModel();
        try{
            if(editExpPurposeInput.getText().trim().equals("")){
                msgLabelEditExp.setText("Expense purpose cannot be blank.");
                msgLabelEditExp.setForeground(Color.red);
            }else{
                msgLabelEditExp.setText("");
                Expense expense = new Expense(editExpPurposeInput.getText(),Integer.parseInt(editExpAmtInput.getText()));
                editExpTableModel.addRow(new Object[]{editExpPurposeInput.getText(),editExpAmtInput.getText()});
                budgetList.getBudgetItem(budgetIndex).addExpense(expense);

                //update total expense and total balance
                expTableModel.addRow(new Object[]{editExpPurposeInput.getText(),editExpAmtInput.getText()});
                totalExpContent.setText(Integer.toString(budgetList.getBudgetItem(budgetIndex).getTotalExpense()));
                balanceContent.setText(Integer.toString(budgetList.getBudgetItem(budgetIndex).getBalance()));
            }
        }catch(NumberFormatException nfe){
            msgLabelEditExp.setText("Budget amount is not an integer. Please re-enter.");
            msgLabelEditExp.setForeground(Color.red);
        }catch (ExpensePurposeHasCommaException expenseCommaException){
            msgLabelEditExp.setText(expenseCommaException.purposeCommaMsg());
            msgLabelEditExp.setForeground(Color.red);
        }
    }//GEN-LAST:event_editExpInsertBtnActionPerformed

