    private void addDataToExpenseTable(JTable jTable){
        DefaultTableModel expTableModel = (DefaultTableModel) jTable.getModel();
        Object rowData[] = new Object[2];
        for(int i = 0; i < budgetList.getBudgetItem(budgetIndex).getExpenseListSize();i++){
            rowData[0] = budgetList.getBudgetItem(budgetIndex).getExpenseItem(i).getExpensePurpose();
            rowData[1] = budgetList.getBudgetItem(budgetIndex).getExpenseItem(i).getExpenseAmount();
            expTableModel.addRow(rowData);
        }
    }

